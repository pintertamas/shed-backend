    @Autowired
    private JavaMailSender javaMailSender;

    public void sendMessage(String to, String subject, String text) {
        SimpleMailMessage message = new SimpleMailMessage();
        message.setFrom("noreplyshed@gmail.com");
        message.setTo(to);
        message.setSubject(subject);
        message.setText(text);
        javaMailSender.send(message);
    }

    public boolean sendRegistrationMessage(String toEmail, int oneTomePassword) {
        try {
            MimeMessage msg = javaMailSender.createMimeMessage();
            MimeMessageHelper helper = new MimeMessageHelper(msg, true);
            helper.setTo(new InternetAddress(toEmail.trim().toLowerCase(Locale.ROOT), false));
            helper.setSubject("Registration");
            helper.setText(generateRegistrationTextWithOneTimePassword(oneTomePassword), true);
            javaMailSender.send(msg);
            return true;
        } catch (MessagingException e) {
            LoggerFactory.getLogger(this.getClass()).error(e.getMessage());
            return false;
        }
    }

